
public class SwapNo {

	public static void main(String[] args) {
		
		int no1 = 10,no2 =20,temp=0;
		System.out.println("no1 = "+no1);
		System.out.println("no2 = "+no2);
		
		//temp = 10
		temp = no1;
		//no1 = 20
		no1 = no2;
		//no2  =10
		no2 = temp;
		
		System.out.println("no1 = "+no1);
		System.out.println("no2 = "+no2);
		
		
	}
}
